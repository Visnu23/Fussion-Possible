def evaluate(m, loader):
    m.eval()
    correct = total = 0
    with torch.inference_mode():
        for xb, yb in loader:
            xb, yb = xb.to(device), yb.to(device)
            pred = m(xb).argmax(1)
            correct += (pred == yb).sum().item()
            total += yb.size(0)
    return correct / max(1, total)

EPOCHS = 15  # ⬅️ adjust to 20–30 for ~98% accuracy
