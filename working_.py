# 🔑 Normalize class label for advice lookup
def normalize_label(label: str):
    return (
        label.strip()
             .replace(" ", "_")
             .replace("-", "_")
             .replace("___", "_")
             .replace("__", "_")
    )


def is_soil_image(pil_img, brown_threshold=0.08):
    """
    Rough soil detection using brown pixel ratio.
    """
    img = np.array(pil_img.convert("RGB"))
    hsv = cv2.cvtColor(img, cv2.COLOR_RGB2HSV)

    # brown color range
    lower_brown = np.array([10, 40, 20])
    upper_brown = np.array([25, 255, 200])

    mask_brown = cv2.inRange(hsv, lower_brown, upper_brown)
    brown_ratio = (mask_brown > 0).sum() / mask_brown.size

    return brown_ratio > brown_threshold

def center_crop_focus(pil_img):
    img = np.array(pil_img)
    h, w, _ = img.shape

    # Crop central 70% region
    crop = img[
        int(h*0.15):int(h*0.85),
        int(w*0.15):int(w*0.85)
    ]

    return Image.fromarray(crop)


@torch.inference_mode()
def predict_image(pil_img: Image.Image):

    x = infer_tfm(pil_img).unsqueeze(0).to(device)

    logits = MODEL(x)
    probs = torch.softmax(logits, dim=1)[0].cpu().numpy()

    # Get top-2 predictions
    top2 = np.argsort(probs)[-2:][::-1]
    idx1, idx2 = top2[0], top2[1]

    label1 = CLASSES[idx1]
    conf1 = float(probs[idx1])
    conf2 = float(probs[idx2])

    norm_label = normalize_label(label1)

    # -----------------------------
    # SMART CONFIDENCE LOGIC
    # -----------------------------
    confidence_margin = conf1 - conf2

    # If prediction is very weak AND margin small → invalid
    if conf1 < 0.40 or confidence_margin < 0.10:
        return {
            "type": "Invalid",
            "label": "Invalid Input",
            "confidence": conf1,
            "severity_score": 0.0,
            "severity_stage": "N/A",
            "advice": {
                "summary": "The image is not clearly recognized as supported soil or crop.",
                "actions": ["Upload a clear soil surface or crop leaf image."],
                "prevention": []
            }
        }

    # -----------------------------
    # TYPE DECISION BY CLASS NAME
    # -----------------------------
    soil_classes = {
        "Black_Soil",
        "Cinder_Soil",
        "Laterite_Soil",
        "Peat_Soil",
        "Yellow_Soil"
    }

    if norm_label in soil_classes:

        rec = ADVICE.get(norm_label)

        return {
            "type": "Soil",
            "label": norm_label,
            "confidence": conf1,
            "severity_score": 0.0,
            "severity_stage": "N/A",
            "advice": rec
        }

    else:
        sev_score, stage = estimate_severity_pil(pil_img)
        rec = ADVICE.get(norm_label, ADVICE.get("Other_Crop"))

        return {
            "type": "Leaf",
            "label": norm_label,
            "confidence": conf1,
            "severity_score": sev_score,
            "severity_stage": stage,
            "advice": rec
        }

